import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import type { Picture } from "vite-imagetools";
import { dev } from "$app/environment";
import { getHardcoverBooks } from "./hardcover.server";
import type { Item } from "./item.server";
import { createItem } from "./item.server";
import { getLiteralDataForIsbn } from "./literal.server";

let bookPictures: Record<string, Picture> = {};
let lastCacheCheck: Record<string, number> = {};

// Load existing enhanced images and cache metadata
const loadBookPictures = async () => {
	try {
		const { bookPictures: imported } = await import("$lib/generated/book-pictures.js");
		bookPictures = imported;

		// Load cache metadata
		const metadataPath = join(process.cwd(), "src/lib/generated/book-cache.json");
		if (existsSync(metadataPath)) {
			const metadata = JSON.parse(readFileSync(metadataPath, "utf-8"));
			lastCacheCheck = metadata.lastCacheCheck || {};
		}
	} catch {
		if (!dev) {
			console.warn("Could not import book pictures, using fallback URLs");
		}
	}
};

// Initialize on module load
loadBookPictures().catch(() => {
	if (!dev) {
		console.warn("Could not load book pictures on module init");
	}
});

// Background image caching function
const downloadImage = async (url: string, filePath: string): Promise<boolean> => {
	try {
		const response = await fetch(url);
		if (!response.ok) throw new Error(`HTTP ${response.status}`);

		const arrayBuffer = await response.arrayBuffer();
		const buffer = Buffer.from(arrayBuffer);
		writeFileSync(filePath, buffer);
		return true;
	} catch (error) {
		console.warn(`Failed to download ${url}:`, error);
		return false;
	}
};

// Smart cache function - only downloads new or stale images
const cacheBookImages = async (books: Array<{ isbn: string; url: string; title: string }>) => {
	if (books.length === 0) return;

	const now = Date.now();
	const oneMonth = 30 * 24 * 60 * 60 * 1000; // 30 days in milliseconds

	// Filter books that need caching (new or stale)
	const booksToCache = books.filter(({ isbn }) => {
		const lastCheck = lastCacheCheck[isbn] || 0;
		const isStale = now - lastCheck > oneMonth;
		const isNew = !bookPictures[isbn];
		return isNew || isStale;
	});

	if (booksToCache.length === 0) {
		console.log(`Library: ${books.length} book thumbnails are up to date`);
		return;
	}

	console.log(
		`Library: Caching ${booksToCache.length}/${books.length} book thumbnail (${booksToCache.length - books.filter((b) => !bookPictures[b.isbn]).length} stale, ${books.filter((b) => !bookPictures[b.isbn]).length} new)...`,
	);

	// Create directories
	const imagesDir = join(process.cwd(), "src/lib/images");
	const generatedDir = join(process.cwd(), "src/lib/generated");

	if (!existsSync(imagesDir)) mkdirSync(imagesDir, { recursive: true });
	if (!existsSync(generatedDir)) mkdirSync(generatedDir, { recursive: true });

	// Download new/stale images
	const successful: Array<{ isbn: string; fileName: string }> = [];

	// First add existing cached images that don't need updating
	books.forEach(({ isbn }) => {
		if (bookPictures[isbn] && !booksToCache.find((b) => b.isbn === isbn)) {
			successful.push({ isbn, fileName: `book-${isbn}.jpg` });
		}
	});

	// Download new/stale images
	for (const { isbn, url, title } of booksToCache) {
		const fileName = `book-${isbn}.jpg`;
		const filePath = join(imagesDir, fileName);

		if (await downloadImage(url, filePath)) {
			successful.push({ isbn, fileName });
			lastCacheCheck[isbn] = now;
			console.log(`  ✓ Cached: ${title}`);
		}
	}

	// Generate updated mappings file with enhanced parameters
	const imports = successful
		.map(
			({ isbn, fileName }) =>
				`// @ts-expect-error\nimport book${isbn.replace(/\D/g, "")} from '../images/${fileName}?enhanced&w=240&format=webp;jpg';`,
		)
		.join("\n");

	const exports = successful
		.map(({ isbn }) => `  '${isbn}': book${isbn.replace(/\D/g, "")},`)
		.join("\n");

	const mappingsContent = `// This file is auto-generated by src/routes/library/books.server.ts
// Do not edit manually - changes will be overwritten
// Generated on: ${new Date().toISOString()}

import type { Picture } from "vite-imagetools";

${imports}

export const bookPictures: Record<string, Picture> = {
${exports}
};
  `;

	writeFileSync(join(generatedDir, "book-pictures.js"), mappingsContent);

	// Save cache metadata
	writeFileSync(
		join(generatedDir, "book-cache.json"),
		JSON.stringify({ lastCacheCheck, lastUpdated: now }, null, 2),
	);
};

export const getBooks = async (): Promise<Item[]> => {
	const ratedBooks = await getHardcoverBooks();

	if (ratedBooks.length === 0) {
		console.warn("No books returned from Hardcover API");
		return [];
	}

	const booksToCache: Array<{ isbn: string; url: string; title: string }> = [];

	const bookPromises = ratedBooks.map(async (book) => {
		const isbns = book.editionIsbns.filter(Boolean);
		if (isbns.length === 0) {
			console.warn(`No ISBNs found for book: ${book.title}`);
			return null; // Skip this book
		}

		// Find first valid ISBN with cover data
		let literalData = null;
		let isbn = null;
		for (const currentIsbn of isbns) {
			const { data } = await getLiteralDataForIsbn(currentIsbn);
			if (data?.cover) {
				literalData = data;
				isbn = currentIsbn;
				break; // Stop once cover found
			}
		}

		if (!literalData || !isbn) {
			console.warn(`No Literal data found for any ISBN for book: ${book.title}`);
			return null; // Skip this book
		}

		try {
			// Collect book for potential caching
			booksToCache.push({
				isbn,
				url: literalData.cover,
				title: `${book.title}${book.subtitle ? `, ${book.subtitle}` : ""}`,
			});

			// Use enhanced image - all books should have cached enhanced images
			const thumbnail = bookPictures[isbn];

			return createItem("book", {
				title: `${book.title}${book.subtitle ? `, ${book.subtitle}` : ""}`,
				published: book.releaseYear.toString() + "-01-01",
				published_by: literalData.authors?.map((author) => author.name).join(", ") || "Unknown",
				thumbnail,
				note: book.reviewText,
			});
		} catch (error) {
			console.warn(`Failed to create book item:`, error);
			return null;
		}
	});

	const books = (await Promise.all(bookPromises)).filter(Boolean) as Item[];

	// Cache images before returning books
	if (booksToCache.length > 0) {
		await cacheBookImages(booksToCache);
	}

	return books;
};
